classmates = ['Michael', 'Bob', 'Tracy']
#输入列表全部名称
print('classmates =', classmates)
#显示列表长度
print('len(classmates) =', len(classmates))
#输出列表中第一项
print('classmates[0] =', classmates[0])
#输出列表中的第二项
print('classmates[1] =', classmates[1])
#输出列表中的第三项
print('classmates[2] =', classmates[2])
#输出列表中最后一项
print('classmates[-1] =', classmates[-1])
#移除列表中的一个元素 默认最后一项
classmates.pop()
print('classmates =', classmates)